package mypack;

public class Answers {
	private String answer, postedby;

	public Answers(String answer, String postedby) {
		super();
		this.answer = answer;
		this.postedby = postedby;
	}

	@Override
	public String toString() {
		return "Answers [answer=" + answer + ", postedby=" + postedby + "]";
	}
	
	
	
	

}
